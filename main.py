# Доиашнее задание по теме "Строки и Индексация строк"

# В переменную example записываем строку
example ='Топинамбур'

# Выводим первый символ строки
print(example[0])

# Выводим последний символ строки с использованием отрицательного индекса
print(example[-1])

# Выводим вторую половину строки
half_length = len(example) // 2
print(example[half_length:])

# Выводим строку задом наперед
print(example[::-1])

# Выводим каждый второй символ строки
print(example[::2])


